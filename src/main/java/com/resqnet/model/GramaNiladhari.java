package com.resqnet.model;

public class GramaNiladhari {
    private int userId;
    private String name;
    private String contactNumber;
    private String address;
    private String gnDivision;
    private String serviceNumber;
    private String gnDivisionNumber;

    public GramaNiladhari() {}

    public int getUserId() { return userId; }
    public void setUserId(int userId) { this.userId = userId; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getContactNumber() { return contactNumber; }
    public void setContactNumber(String contactNumber) { this.contactNumber = contactNumber; }

    public String getAddress() { return address; }
    public void setAddress(String address) { this.address = address; }

    public String getGnDivision() { return gnDivision; }
    public void setGnDivision(String gnDivision) { this.gnDivision = gnDivision; }

    public String getServiceNumber() { return serviceNumber; }
    public void setServiceNumber(String serviceNumber) { this.serviceNumber = serviceNumber; }

    public String getGnDivisionNumber() { return gnDivisionNumber; }
    public void setGnDivisionNumber(String gnDivisionNumber) { this.gnDivisionNumber = gnDivisionNumber; }
}
